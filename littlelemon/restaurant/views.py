def book(request):
    form = BookingForm()
    if request.method == 'POST':
        form = BookingForm(request.POST)
        if form.is_valid():
            booking_time = form.cleaned_data['reservation_time']
            booking_date = form.cleaned_data['reservation_date']
            # Check if there's an existing booking at this time
            if Booking.objects.filter(reservation_time=booking_time).exists() and Booking.objects.filter(reservation_date=booking_date).exists():
                messages.error(request, 'This time slot is already booked.')
            else:
                form.save()
                messages.success(request, 'Your booking was successful!')
                form = BookingForm() 
    bookings = Booking.objects.all()
    booking_data = {"bookings":bookings}
    context = {'form':form, 'booking_data':booking_data}
    return render(request, 'book.html', context)


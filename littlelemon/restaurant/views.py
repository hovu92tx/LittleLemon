class BookingsView(generics.ListCreateAPIView):
    queryset = Booking.objects.all()
    serializer_class = BookingSerializer
    def get_queryset(self):
        queryset = Booking.objects.all()
        reservation_date = self.request.query_params.get('reservation_date')
        if reservation_date:
            date_obj = parse_date(reservation_date)
            if date_obj:
                queryset = queryset.filter(reservation_date=date_obj)
        return queryset


